package model;

public class Physio
{
    String staffid;
    String phyName;
    int phyRate;
    String phyAge;
    String phyGender;
    String phyHos;
    String phyPos;
    String btime;

    public Physio(){}

    public String getStaffid() {
        return staffid;
    }

    public void setStaffid(String staffid) {
        this.staffid = staffid;
    }

    public String getPhyName() {
        return phyName;
    }

    public void setPhyName(String phyName) {
        this.phyName = phyName;
    }

    public int getPhyRate() {
        return phyRate;
    }

    public void setPhyRate(int phyRate) {
        this.phyRate = phyRate;
    }

    public String getPhyAge() {
        return phyAge;
    }

    public void setPhyAge(String phyAge) {
        this.phyAge = phyAge;
    }

    public String getPhyGender() {
        return phyGender;
    }

    public void setPhyGender(String phyGender) {
        this.phyGender = phyGender;
    }

    public String getPhyHos() {
        return phyHos;
    }

    public void setPhyHos(String phyHos) {
        this.phyHos = phyHos;
    }

    public String getPhyPos() {
        return phyPos;
    }

    public void setPhyPos(String phyPos) {
        this.phyPos = phyPos;
    }

    public String getBtime() {
        return btime;
    }

    public void setBtime(String btime) {
        this.btime = btime;
    }
}
